2. “쿠폰”은 현금 가치가 없으며, 쿠폰의 사용기한이 지나거나 “회원” 탈퇴 및 자격이 상실된 경우 회수됩니다.
3. “쿠폰”이 사용된 예약이 취소된 경우 회사의 정책에 따라 “쿠폰”의 환원 여부가 결정됩니다.
4. “쿠폰”은 타인에게 양도할 수 없으며, 유상으로 거래하거나 현금 또는 “캐시”로 전환될 수 없습니다.
5. “회사”가 정하지 않은 방법으로 “쿠폰”을 발급/사용한 사실이 확인될 경우 “회사”는 발급된 “쿠폰”을 회수하거나 부정 발급된 “쿠폰”을 사용한 예약을 취소할 수 있습니다.

제 28 조 (캐시)
1. “캐시”는 “회사”의 캐시 적립 정책에 따라 “회원”에게 적립되며, “캐시”의 적립 정책 및 사용방법, 사용기한과 같은 제반 사항은 “서비스” 화면에 게시하거나 통지합니다.
2. “회원”은 트립빌더 앱에서 결제하는 여행상품 중 “캐시” 사용이 가능한 일부 상품 예약 시 “캐시”를 사용할 수 있습니다.
3. “캐시”는 사용기한이 지나거나 “회원” 탈퇴 및 자격이 상실된 경우 자동으로 소멸되며, 탈퇴 이후 재가입하더라도 소멸된 “캐시”는 복구되지 않습니다.
4. “캐시”는 타인에게 양도할 수 없으며, 현금 및 현금화되는 수단으로 환급될 수 없습니다.
5. “회사”가 정하지 않은 방법으로 “캐시”를 적립/사용한 사실이 확인될 경우 “회사”는 적립된 “캐시”를 환수하거나 부정 적립된 “캐시”를 사용한 예약을 취소할 수 있습니다.
6. “회사”는 본 조의 이용제한 범위 내에서 제한의 조건 및 세부내용은 개별 서비스상의 운영정책에서 정하는 바에 의합니다.

제 29 조 (계약해제, 해지 등)
1. “회원”은 언제든지 서비스초기화면의 고객센터 또는 내 정보 관리 메뉴 등을 통하여 이용계약 해지 신청을 할 수 있으며, “회사”는 관련법 등이 정하는 바에 따라 이를 즉시 처리하여야 합니다. 다만, 해지의사를 통지하기 전에 모든 재화의 판매 및 구매 절차를 완료, 철회 또는 취소해야만 합니다. 이 경우 판매 및 구매의 철회 또는 취소로 인한 불이익은 회원 본인이 부담하여야 합니다.
2. “회원”이 계약을 해지할 경우, 관련법 및 개인정보처리방침에 따라 “회사”가 회원정보를 보유하는 경우를 제외하고는 해지 즉시 “회원”의 모든 데이터는 소멸됩니다.
3. “회원”이 계약을 해지하는 경우, “회원”이 작성한 “게시물” 중 본인 계정에 등록된 게시물 일체는 삭제됩니다. 다만, 타인의 게시물 또는 공용게시판에 등록된 “게시물" 등은 삭제되지 않으니 사전에 삭제 후 탈퇴해야 합니다.
4. 이용계약 해지로 인해 발생한 불이익에 대한 책임은 회원 본인이 부담하여야 하며, 이용계약이 종료되면 “회사”는 회원에게 부가적으로 제공한 각종 무상혜택을 회수할 수 있습니다.
5. 회원이 다음 각 호의 사유에 해당하는 경우, “회사”는 이용계약을 해지할 수 있습니다.
(1) 가입 신청 시에 허위 내용을 등록한 경우
(2) “서비스”를 이용하여 구입한 재화 등의 대금, 기타 “서비스”이용에 관련하여 회원이 부담하는 채무를 기일에 지급하지 않는 경우
(3) 다른 사람의 “서비스” 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우
(4) “서비스”를 이용하여 법령 또는 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우

6. “회사”가 회원 자격을 제한․정지 시킨 후, 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 “회사”는 이용계약을 해지할 수 있습니다.
7. “회사”가 이용계약을 해지하는 경우 “회사”는 회원에게 이를 통지하고, 회원등록 말소 전에 최소한 30일 이상의 기간을 정하여 소명할 기회를 부여합니다. 다만, 이 약관 및 운영정책에서 정하는 사유가 있을 경우에는 별도의 이의 신청 기간을 부여하지 않을 수 있습니다.
8. 이용계약이 종료됨으로써 발생한 손해는 이용계약 종료사유에 대해 귀책사유가 있는 회원이 책임을 부담하여야 하고, “회사”는 관련 법령에 규정이 없는 한 책임을 지지 않습니다.

제 30 조 (이용제한 등)
1. “회사”는 “회원”이 이 약관의 의무를 위반하거나 “서비스”의 정상적인 운영을 방해한 경우, 경고, 일시정지, 영구이용정지 등으로 “서비스” 이용을 단계적으로 제한할 수 있습니다.
